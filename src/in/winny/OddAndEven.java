 package in.winny;

public class OddAndEven {
	public static void main(String[] args) {
		int num=10;
		 
		//Odd number
		for (int i = 0; i <=num; i++) {
			if (i%2==1) {
				 System.out.println(i);
			}
			
		}
		
		
		System.out.println("************************************");
		int num1=20;
		//Even Number
		for (int i = 0; i <=num1; i++) {
			if (i%2==0) {
				 System.out.println(i);
			}
			
		}
	}
	

}
