package in.winny;

public class PostIncrement {
	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		System.out.println("****************");
		a++;
		System.out.println("****************");
		System.out.println(a++);
		System.out.println("****************");
		System.out.println(a);
	}
}
